#include <unistd.h>

void	ft_print_comb(void)
{
	int	r;
	int	u;
	int	i;

	r = '0';
	while (r <= '7')
	{
		u = r + 1;
		while (u <= '8')
		{
			i = u + 1;
			while (i <= '9')
			{
				write(1, &r, 1);
				write(1, &u, 1);
				write(1, &i, 1);
				if (r != '7' || u != '8' || i != '9')
					write(1, ", ", 2);
				i++;
			}
			u++;
		}
		r++;
	}
}
